//Igaul (==)
//Retorna verdadeiro caso os operadores sejam iguais
"3" == var1
3 == '3' //false

//Não igaul (!=)
//Retorna verdadeiro caso os operadores não sejam iguais
var2 != "3"

//Estritamente igual (===)
//Retorna verdadeiro caso os operadores sejam iguais e do mesmo tipo.
//Veja também Object.is
3 === var1

//Estritamente não igual (!==)
//Retorna verdadeiro caso os operadores não sejam iguais e/ou não sejam do mesmo tipo.
var1 !== "3"
3 !== '3'

//Maior que (>)
//Retorna verdadeiro caso o operador da esquerda seja maior que o da direita.
var2 > var1
"12" > 2 //true

//Maior que ou igual (>=)
//Retorna verdadeiro caso o operador da esquerda seja maior ou igual ao da direita.
var2 >= var1
var1 >= 2

//Menor que (<)
//Retorna verdadeiro caso o operador da esquerda seja menor que o da direita.
var2 < var1
"12" < "2"

//Menor que ou igual (<=)
//Retorna verdadeiro caso o operador da esquerda seja menor ou igual ao da direita.
var2 <= var1
var1 <= 2
